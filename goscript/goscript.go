// Represents the code transformed to JavaScript.
type transform struct {
	err      []error
	pointers []string

	bConst *bytes.Buffer
	bType  *bytes.Buffer
	bVar   *bytes.Buffer
	fset   *token.FileSet
}

func newTransform() *transform {
	return &transform{
		nil,
		make([]string, 0),
		new(bytes.Buffer), new(bytes.Buffer), new(bytes.Buffer),
		token.NewFileSet(),
	}
}

// Compiles a Go source file into JavaScript.
// Writes the output in "filename" but with extension ".js".
func Compile(filename string) error {
	trans := newTransform()

	/* Parse several files
	parse.ParseFile(fset, "a.go", nil, 0)
	parse.ParseFile(fset, "b.go", nil, 0)
	*/

	// If Go sintaxis is incorrect then there will be an error.
	node, err := parser.ParseFile(trans.fset, filename, nil, 0) //parser.ParseComments)
	if err != nil {
		return err
	}

	trans.bConst.WriteString("// Generated by GoScript <github.com/kless/GoScript>\n")

	for _, decl := range node.Decls {
		//fmt.Printf("%T : %v\n", decl,decl) //! TODO: to delete when it's finished

		switch decl.(type) {

		// http://golang.org/pkg/go/ast/#GenDecl || godoc go/ast GenDecl
		//  Tok    token.Token   // IMPORT, CONST, TYPE, VAR
		//  Specs  []Spec
		case *ast.GenDecl:
			genDecl := decl.(*ast.GenDecl)

			switch genDecl.Tok {
			case token.CONST:
				trans.getConst(genDecl.Specs)
			case token.TYPE:
				trans.getType(genDecl.Specs)
			case token.VAR:
				trans.getVar(genDecl.Specs)
			}
		}
	}

	if trans.err != nil {
		for _, err := range trans.err {
			fmt.Fprintf(os.Stderr, "%s\n", err.Error())
		}
		return errors.New("Error: not supported in JavaScript")
	}

	// Write all buffers in the first one
	trans.bConst.Write(trans.bType.Bytes())
	trans.bConst.Write(trans.bVar.Bytes())
	fmt.Print(trans.bConst.String()) // TODO: delete

	//jsFile := strings.Replace(filename, path.Ext(filename), ".js", 1)
	//return ioutil.WriteFile(jsFile, trans.bConst.Bytes(), 0664)
	return nil
}
